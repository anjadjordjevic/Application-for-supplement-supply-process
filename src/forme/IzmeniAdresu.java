    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if (jTextField1.getText().isEmpty() || jTextField3.getText().isEmpty()
                || jTextField4.getText().isEmpty() || jTextField5.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Morate popuniti sva polja.");
            return;
        }

        int gradID = Integer.parseInt(jTextField1.getText());
        int drzavaID = Integer.parseInt(jTextField5.getText());
        String ulica = jTextField3.getText();
        int broj = Integer.parseInt(jTextField4.getText());

        Drzava drzava = new Drzava(drzavaID, null);
        Grad grad = new Grad(drzava, gradID, null);
        Adresa a = new Adresa(grad, adresa.getAdresaID(), ulica, broj);

        boolean uspesno = Kontroler.getInstance().updateAdresa(a);

        if (uspesno) {
            JOptionPane.showMessageDialog(this, "Uspešno ste izmenili adresu.");
            AdresaForma af = (AdresaForma) this.getParent();
            af.popuniTabelu();
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Greška pri ažuriranju adrese.");
        }

    }//GEN-LAST:event_jButton1ActionPerformed

